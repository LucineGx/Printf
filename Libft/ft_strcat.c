char	*ft_strcat(char *dest, const char *src)
{
	int i;
	int j;

	i = -1;
	while (dest[++i]);
	j = 1;
	while (src[i - j])
	{
		dest[i] = src[i - j];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
